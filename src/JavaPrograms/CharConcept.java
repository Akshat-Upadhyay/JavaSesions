package JavaPrograms;

public class CharConcept {

	public static void main(String[] args) {

		char c1='a';
		char c2='b';
		
		System.out.println(c1+c2);//When you perform arithmetic operation on character, instead of adding, it converts them into ASCII value
		System.out.println(c1);
		
		System.out.println(c2);
		
		//ASCII Range	
		//a-z: 97-122
		//A-Z: 65-90
		//0-9: 48-57
		
		String S1="a";
		String S2="b";
		
		System.out.println(S1+S2);
		


	}

}
